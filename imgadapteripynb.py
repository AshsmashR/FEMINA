def extract_gold_assistant_text(messages):
    # find last assistant message that has "content"
    for m in reversed(messages):
        if m.get("role") == "assistant":
            c = m.get("content")
            if isinstance(c, str):
                return c.strip()
            # if content is list/structured, you can adapt here
            return str(c).strip()
    return None

def simple_exact_match(eval_ds, n=100):
    n = min(n, len(eval_ds))
    hit = 0
    total = 0
    for i in range(n):
        ex = eval_ds[i]
        gold = extract_gold_assistant_text(ex["messages"])
        if gold is None:
            continue
        pred = generate_from_example(ex, max_new_tokens=256, temperature=0.0)
        # very strict; you can loosen with contains/normalize if needed
        if pred.strip() == gold.strip():
            hit += 1
        total += 1
    return (hit / total) if total > 0 else None

