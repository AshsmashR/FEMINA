@torch.no_grad()
def generate_from_example(ex, max_new_tokens=256, temperature=0.2, top_p=0.9):
    # Build the exact same text format you trained on
    prompt = processor.apply_chat_template(
        ex["messages"],
        add_generation_prompt=True,   # IMPORTANT for inference
        tokenize=False
    ).strip()

    img = ex["image"].convert("RGB")
    inputs = processor(
        text=[prompt],
        images=[[img]],
        return_tensors="pt",
        padding=True
    )
    device = next(infer_model.parameters()).device
    inputs = {k: v.to(device) for k, v in inputs.items()}

    gen_ids = infer_model.generate(
        **inputs,
        max_new_tokens=max_new_tokens,
        do_sample=(temperature is not None and temperature > 0),
        temperature=temperature,
        top_p=top_p,
        use_cache=True
    )
    out_text = processor.tokenizer.decode(gen_ids[0], skip_special_tokens=True)

    return out_text

