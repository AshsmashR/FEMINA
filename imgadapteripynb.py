def collate_fn(examples):
    texts = []
    images = []
    for ex in examples:
        # processor expects list of images per example
        images.append([ex["image"].convert("RGB")])
        text = processor.apply_chat_template(
            ex["messages"], add_generation_prompt=False, tokenize=False
        ).strip()
        texts.append(text)

    batch = processor(text=texts, images=images, return_tensors="pt", padding=True)

    labels = batch["input_ids"].clone()
    labels[labels == processor.tokenizer.pad_token_id] = -100

    # Mask image tokens
    boi_token_id = processor.tokenizer.convert_tokens_to_ids(
        processor.tokenizer.special_tokens_map["boi_token"]
    )
    labels[labels == boi_token_id] = -100

    batch["labels"] = labels
    return batch

