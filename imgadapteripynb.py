def load_one_dataset(name: str):
    root = DATA_ROOTS[name]
    json_path = os.path.join(root, JSONL_FILES[name])

    # Load jsonl
    ds = load_dataset("json", data_files=json_path, split="train")

    # Cast image column to PIL (with base directory awareness)
    def add_full_path(example):
        image_found = False
        for message in example.get("messages", []):
            if message.get("role") == "user" and "content" in message:
                for content_item in message["content"]:
                    if content_item.get("type") == "image" and "image_url" in content_item:
                        rel_path = content_item["image_url"]
                        full_path = os.path.join(root, rel_path)
                        example["image_path"] = full_path
                        image_found = True
                        break # Found the image, exit content loop
            if image_found:
                break # Found the image, exit messages loop
        return example

    ds = ds.map(add_full_path)
    ds = ds.cast_column("image_path", Image())   # this loads PIL images
    ds = ds.rename_column("image_path", "image") # now standard "image" column

    # Optional: if your json already has train/val/test marker
    if HAS_SPLIT_FIELD:
        splits = ds.train_test_split(test_size=0.0, stratify_by=SPLIT_KEY) # dummy
        # You'd filter instead:
        train_ds = ds.filter(lambda x: x[SPLIT_KEY] == "train")
        val_ds   = ds.filter(lambda x: x[SPLIT_KEY] == "val")
        test_ds  = ds.filter(lambda x: x[SPLIT_KEY] == "test")
    else:
        # Manual split (stratified if you have labels)
        train_val_test = ds.train_test_split(test_size=0.15, seed=42)
        train_val      = train_val_test["train"].train_test_split(test_size=0.15/(1-0.15), seed=42)

        train_ds = train_val["train"]
        val_ds   = train_val["test"]
        test_ds  = train_val_test["test"]

    return DatasetDict({
        "train": train_ds,
        "validation": val_ds,
        "test": test_ds
    })


