def sanitize_output(text, max_lines=30, max_chars=1800):
    # Trim huge rambles
    text = text.strip()[:max_chars]

    # Remove repeated identical lines (common looping)
    lines = [ln.rstrip() for ln in text.splitlines()]
    out = []
    seen = set()
    for ln in lines:
        key = ln.strip()
        if key and key in seen:
            continue
        if key:
            seen.add(key)
        out.append(ln)
        if len(out) >= max_lines:
            break
    return "\n".join(out).strip()

