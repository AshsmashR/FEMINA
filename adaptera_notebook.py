def generate_clean(instruction, inp="", max_new_tokens=180):
    # Hard constraint instruction (no retraining needed)
    instruction2 = instruction.strip() + "\n\nReturn ONLY the answer in the same format. Do not add extra definitions. Stop after the final line."

    prompt = build_prompt(instruction2, inp)
    inputs = tokenizer(prompt, return_tensors="pt").to(model.device)

    with torch.no_grad():
        out = model.generate(
            **inputs,
            max_new_tokens=max_new_tokens,
            do_sample=False,               # deterministic
            repetition_penalty=1.12,       # reduces looping
            no_repeat_ngram_size=4,        # helps stop repeated chunks
            eos_token_id=tokenizer.eos_token_id,
            pad_token_id=tokenizer.pad_token_id,
        )

    text = tokenizer.decode(out[0], skip_special_tokens=True)
    resp = text.split("### Response:", 1)[-1].strip()

    # Optional: post-clean (remove accidental reprints of Instruction/Input)
    cut_markers = ["### Instruction:", "### Input:"]
    for m in cut_markers:
        if m in resp:
            resp = resp.split(m, 1)[0].strip()

    return resp

