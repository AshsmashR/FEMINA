def generate_from_val_text(model, full_text, max_new_tokens=180):
    prompt_only = full_text.split(RESPONSE_TAG, 1)[0] + RESPONSE_TAG
    inputs = tokenizer(prompt_only, return_tensors="pt").to(model.device)

    with torch.no_grad():
        gen = model.generate(
            **inputs,
            max_new_tokens=max_new_tokens,
            do_sample=False,
            repetition_penalty=1.12,
            no_repeat_ngram_size=4,
            eos_token_id=tokenizer.eos_token_id,
            pad_token_id=tokenizer.pad_token_id,
        )

    decoded = tokenizer.decode(gen[0], skip_special_tokens=True)
    resp = decoded.split("### Response:", 1)[-1].strip()

    # stop if it starts reprinting the prompt
    for marker in ["### Instruction:", "### Input:"]:
        if marker in resp:
            resp = resp.split(marker, 1)[0].strip()

    return resp

