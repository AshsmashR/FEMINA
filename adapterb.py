RESPONSE_TAG = "### Response:\n"

def make_eval_item(text, max_len=2048):
    enc = tokenizer(text, add_special_tokens=False, truncation=True, max_length=max_len)
    input_ids = enc["input_ids"]
    attn = enc["attention_mask"]

    idx = text.find(RESPONSE_TAG)
    if idx == -1:
        # fallback: score all tokens
        labels = input_ids.copy()
    else:
        prompt_text = text[: idx + len(RESPONSE_TAG)]
        prompt_ids = tokenizer(prompt_text, add_special_tokens=False).input_ids
        labels = [-100]*len(prompt_ids) + input_ids[len(prompt_ids):]
        labels = labels[:len(input_ids)]

    return {
        "input_ids": torch.tensor(input_ids, dtype=torch.long),
        "attention_mask": torch.tensor(attn, dtype=torch.long),
        "labels": torch.tensor(labels, dtype=torch.long),
    }

def collate_pad(features):
    maxlen = max(f["input_ids"].shape[0] for f in features)

    def pad(x, value):
        return torch.nn.functional.pad(x, (0, maxlen - x.shape[0]), value=value)

    input_ids = torch.stack([pad(f["input_ids"], tokenizer.pad_token_id) for f in features])
    attention_mask = torch.stack([pad(f["attention_mask"], 0) for f in features])
    labels = torch.stack([pad(f["labels"], -100) for f in features])

    return {"input_ids": input_ids, "attention_mask": attention_mask, "labels": labels}

def evaluate_on_val(model, val_ds, batch_size=2, max_len=2048):
    losses = []
    with torch.no_grad():
        for i in range(0, len(val_ds), batch_size):
            batch_texts = val_ds[i:i+batch_size]["text"]
            feats = [make_eval_item(t, max_len=max_len) for t in batch_texts]
            batch = collate_pad(feats)
            batch = {k: v.to(model.device) for k, v in batch.items()}
            out = model(**batch)
            losses.append(out.loss.detach().float().item())

    mean_loss = sum(losses) / len(losses)
    ppl = math.exp(mean_loss) if mean_loss < 50 else float("inf")
    return mean_loss, ppl

